package clientes;

/**
 *
 * @author Cesar
 */
public class Clientes {
    protected int CodigoC;
    protected String NomeC;
    protected String EnderecoC;
    protected int NumC;
    protected String TelC;

    /**
     * @return the CodigoC
     */
    public int getCodigoC() {
        return CodigoC;
    }

    /**
     * @param CodigoC the CodigoC to set
     */
    public void setCodigoC(int CodigoC) {
        this.CodigoC = CodigoC;
    }

    /**
     * @return the NomeC
     */
    public String getNomeC() {
        return NomeC;
    }

    /**
     * @param NomeC the NomeC to set
     */
    public void setNomeC(String NomeC) {
        this.NomeC = NomeC;
    }

    /**
     * @return the EnderecoC
     */
    public String getEnderecoC() {
        return EnderecoC;
    }

    /**
     * @param EnderecoC the EnderecoC to set
     */
    public void setEnderecoC(String EnderecoC) {
        this.EnderecoC = EnderecoC;
    }

    /**
     * @return the TelC
     */
    public String getTelC() {
        return TelC;
    }

    /**
     * @param TelC the TelC to set
     */
    public void setTelC(String TelC) {
        this.TelC = TelC;
    }

    /**
     * @return the NumC
     */
    public int getNumC() {
        return NumC;
    }

    /**
     * @param NumC the NumC to set
     */
    public void setNumC(int NumC) {
        this.NumC = NumC;
    }
    
    
    
}
